package ch.eiafr.si.wedding.service;

import ch.eiafr.si.wedding.jpa.GoldenBookEntry;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;
import javax.ws.rs.Consumes;
import javax.ws.rs.DELETE;
import javax.ws.rs.GET;
import javax.ws.rs.POST;
import javax.ws.rs.PUT;
import javax.ws.rs.Path;
import javax.ws.rs.PathParam;
import javax.ws.rs.Produces;

/**
 *
 * @author lucy
 */
@Stateless
@Path("ch.eiafr.si.wedding.jpa.goldenbookentry")
public class GoldenBookEntryFacadeREST extends AbstractFacade<GoldenBookEntry> {
    @PersistenceContext(unitName = "WeddingSiteAppPU")
    private EntityManager em;

    public GoldenBookEntryFacadeREST() {
        super(GoldenBookEntry.class);
    }

    @POST
    @Override
    @Consumes({"application/xml", "application/json"})
    public void create(GoldenBookEntry entity) {
        super.create(entity);
    }

    @PUT
    @Path("{id}")
    @Consumes({"application/xml", "application/json"})
    public void edit(@PathParam("id") Long id, GoldenBookEntry entity) {
        super.edit(entity);
    }

    @DELETE
    @Path("{id}")
    public void remove(@PathParam("id") Long id) {
        super.remove(super.find(id));
    }

    @GET
    @Path("{id}")
    @Produces({"application/xml", "application/json"})
    public GoldenBookEntry find(@PathParam("id") Long id) {
        return super.find(id);
    }

    @GET
    @Override
    @Produces({"application/xml", "application/json"})
    public List<GoldenBookEntry> findAll() {
        return super.findAll();
    }

    @GET
    @Path("{from}/{to}")
    @Produces({"application/xml", "application/json"})
    public List<GoldenBookEntry> findRange(@PathParam("from") Integer from, @PathParam("to") Integer to) {
        return super.findRange(new int[]{from, to});
    }

    @GET
    @Path("count")
    @Produces("text/plain")
    public String countREST() {
        return String.valueOf(super.count());
    }

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }
    
}
